# Faça um programa que receba dois valores A e B. 
# Faça a potência desses dois valores e retorne o resultado:

# a ^ b = z

#%%
valor_a = input("Digite um número (base): ")
valor_b = input("Digite outro número (expoente): ")

valor_a = float(valor_a)
valor_b = float(valor_b)

potencia = valor_a ** valor_b

print(f"O resultado da sua potenciação é: {potencia}")

